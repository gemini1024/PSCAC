package Vo;

public class PSCACVo {

	private String Id;
	private String Gps;
	private String Status;
	
	
	public String getId() {
		return Id;
	}
	public void setId(String id) {
		Id = id;
	}
	public String getGps() {
		return Gps;
	}
	public void setGps(String Gps) {
		Gps = Gps;
	}
	public String getStatus() {
		return Status;
	}
	public void setStatus(String status) {
		Status = status;
	}
}
